    /**
     * Create an entity for this test.
     *
     * This is a static method, as tests for other entities might also need it,
     * if they test an entity which requires the current entity.
     */
    public static Order createEntity(EntityManager em) {
        Order order = new Order().date(DEFAULT_DATE).status(DEFAULT_STATUS).total(DEFAULT_TOTAL);
        // Add required entity
        OrderDetails orderDetails;
        if (TestUtil.findAll(em, OrderDetails.class).isEmpty()) {
            orderDetails = OrderDetailsResourceIT.createEntity(em);
            em.persist(orderDetails);
            em.flush();
        } else {
            orderDetails = TestUtil.findAll(em, OrderDetails.class).get(0);
        }
        order.getOrderDetails().add(orderDetails);
        // Add required entity
        Client client;
        if (TestUtil.findAll(em, Client.class).isEmpty()) {
            client = ClientResourceIT.createEntity(em);
            em.persist(client);
            em.flush();
        } else {
            client = TestUtil.findAll(em, Client.class).get(0);
        }
        order.setClient(client);
        return order;
    }

